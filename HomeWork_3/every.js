// 37. Using every() create a function that is checking if all
// employees are engineers
function everyEngineers(employees){
    return employees.every(elem => elem.isEngineer)
 }
 console.log(everyEngineers(employees))




// 38. Write a function to check if all employees are Male
function everyMale(employees){
    return employees.every(elem => elem.gender === 'Male')
 }
